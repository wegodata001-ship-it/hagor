type TKey = keyof (typeof translations)["en"];

type Ctx = {
  lang: AdminLang;
  setLang: (l: AdminLang) => void;
  t: (k: TKey) => string;
};

const AdminI18nContext = createContext<Ctx | null>(null);

export function AdminI18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<AdminLang>("en");

  useEffect(() => {
    try {
      const raw = localStorage.getItem(STORAGE_KEY);
      if (raw === "en" || raw === "he" || raw === "ar") setLangState(raw);
    } catch {
      // ignore
    }
  }, []);

  const setLang = useCallback((l: AdminLang) => {
    setLangState(l);
    try {
      localStorage.setItem(STORAGE_KEY, l);
    } catch {
      // ignore
    }
  }, []);

  const value = useMemo<Ctx>(() => {
    return {
      lang,
      setLang,
      t: (k) => t(k, lang),
    };
  }, [lang, setLang]);

  return React.createElement(AdminI18nContext.Provider, { value }, children);
}

export function useAdminI18n() {
  const ctx = useContext(AdminI18nContext);
  if (!ctx) throw new Error("useAdminI18n must be used within AdminI18nProvider");
  return ctx;
}

export function isRtl(lang: AdminLang) {
  return lang === "he" || lang === "ar";
}

export function t(key: string, lang: AdminLang = "en"): string {
  return translations[lang]?.[key] ?? translations.en?.[key] ?? key;
}

